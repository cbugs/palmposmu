<script setup>
import { onMounted, ref } from 'vue';

const isGenerating = ref(false);

const loadCSS = (href) => {
  return new Promise((resolve, reject) => {
    const link = document.createElement('link');
    link.rel = 'stylesheet';
    link.href = href;
    link.onload = resolve;
    link.onerror = reject;
    document.head.appendChild(link);
  });
};

const loadScript = (src) => {
  return new Promise((resolve, reject) => {
    const script = document.createElement('script');
    script.src = src;
    script.onload = resolve;
    script.onerror = reject;
    document.head.appendChild(script);
  });
};

onMounted(async () => {
  // Load PrimeIcons, jsPDF and html2canvas from CDN
  try {
    await loadCSS('https://unpkg.com/primeicons@7.0.0/primeicons.css');
    await loadScript('https://cdnjs.cloudflare.com/ajax/libs/html2canvas/1.4.1/html2canvas.min.js');
    await loadScript('https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js');
    console.log('Libraries loaded');
  } catch (error) {
    console.error('Failed to load libraries:', error);
  }
});

const generatePDF = async () => {
  if (isGenerating.value) return;
  
  isGenerating.value = true;
  
  try {
    const { jsPDF } = window.jspdf;
    const html2canvas = window.html2canvas;
    
    if (!jsPDF || !html2canvas) {
      alert('PDF libraries not loaded yet. Please try again.');
      return;
    }
    
    const pdf = new jsPDF('p', 'mm', 'a4');
    const imgWidth = 210; // A4 width in mm
    const pageHeight = 297; // A4 height in mm
    
    // Get all sections to capture separately
    const sections = [
      document.querySelector('.cover-page'),
      document.querySelector('.hero'),
      document.querySelector('.overview-section'),
      document.querySelector('.pricing-section'),
      document.querySelector('.hardware-section'),
      document.querySelector('.industries-section')
    ];
    
    // Add footer to show at bottom of each page
    const footer = document.querySelector('.pdf-footer');
    const disclaimer = document.querySelector('.pdf-disclaimer');
    
    let isFirstPage = true;
    
    for (let i = 0; i < sections.length; i++) {
      const section = sections[i];
      const isLastPage = i === sections.length - 1;
      const isCoverPage = i === 0;
      
      if (!section) continue;
      
      // Capture section as canvas
      const canvas = await html2canvas(section, {
        scale: 2,
        useCORS: true,
        logging: false,
        backgroundColor: '#ffffff',
        windowWidth: isCoverPage ? 1200 : 1200,
        windowHeight: isCoverPage ? 1697 : undefined, // A4 aspect ratio (1200 * 297/210 = 1697)
        imageTimeout: 0,
        allowTaint: true
      });
      
      // Calculate image height for this section maintaining aspect ratio
      const imgHeight = (canvas.height * imgWidth) / canvas.width;
      
      // For cover page, maintain aspect ratio; for others, limit height to page height minus footer space
      let finalHeight;
      if (isCoverPage) {
        finalHeight = imgHeight; // Cover page maintains aspect ratio
      } else {
        const maxContentHeight = pageHeight - 20; // Leave space for footer
        finalHeight = Math.min(imgHeight, maxContentHeight);
      }
      
      // Add new page if not first
      if (!isFirstPage) {
        pdf.addPage();
      }
      isFirstPage = false;
      
      // Add section image
      const imgData = canvas.toDataURL('image/jpeg', 0.95);
      const yPosition = 0;
      pdf.addImage(imgData, 'JPEG', 0, yPosition, imgWidth, finalHeight);
      
      // Add footer at bottom of page (except cover page)
      if (footer && !isCoverPage) {
        const footerCanvas = await html2canvas(footer, {
          scale: 2,
          useCORS: true,
          logging: false,
          backgroundColor: '#ffffff',
          windowWidth: 1200
        });
        
        const footerHeight = (footerCanvas.height * imgWidth) / footerCanvas.width;
        const footerData = footerCanvas.toDataURL('image/jpeg', 0.95);
        pdf.addImage(footerData, 'JPEG', 0, pageHeight - footerHeight - 10, imgWidth, footerHeight);
      }
      
      // Add disclaimer only on last page
      if (isLastPage && disclaimer) {
        disclaimer.style.display = 'block';
        const disclaimerCanvas = await html2canvas(disclaimer, {
          scale: 2,
          useCORS: true,
          logging: false,
          backgroundColor: '#ffffff',
          windowWidth: 1200
        });
        
        const disclaimerHeight = (disclaimerCanvas.height * imgWidth) / disclaimerCanvas.width;
        const disclaimerData = disclaimerCanvas.toDataURL('image/jpeg', 0.95);
        const disclaimerY = pageHeight - disclaimerHeight - 5;
        pdf.addImage(disclaimerData, 'JPEG', 0, disclaimerY, imgWidth, disclaimerHeight);
        disclaimer.style.display = 'none';
      }
    }
    
    // Save the PDF
    pdf.save('PalmPOS-Brochure.pdf');
    
  } catch (error) {
    console.error('Error generating PDF:', error);
    alert('Failed to generate PDF. Please try again.');
  } finally {
    isGenerating.value = false;
  }
};
</script>

